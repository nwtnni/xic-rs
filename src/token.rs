use simple_symbol::Symbol;

#[derive(Clone, Debug, PartialEq, Eq)]
pub enum Token {
    IDENT(Symbol),
    CHARACTER(char),
    INTEGER(i64),
    STRING(String),
    USE, 
    IF,
    WHILE,
    ELSE,
    RETURN,
    LENGTH,
    INT,
    BOOL,
    TRUE,
    FALSE,
    ASSIGN,
    NOT,
    MUL,
    HMUL,
    DIV,
    MOD,
    ADD,
    SUB,
    LE,
    LT,
    GE,
    GT,
    EQ,
    NEQ,
    LAND,
    LOR,
    LPAREN,
    RPAREN,
    LBRACK,
    RBRACK,
    LBRACE,
    RBRACE,
    COLON,
    SEMICOLON,
    COMMA,
    UNDERSCORE,
    EOF,
}
